package car_showroom;

public class Vehicle {
    private int capacity;
    private int speed;

    public void setCapacity(int capacity){
        this.capacity = capacity;
    }
    public void setSpeed(int speed){
        this.speed = speed;
    }
    public int getCapacity(){
        return capacity;
    }
    public int getSpeed(){
        return speed;
    }
}
